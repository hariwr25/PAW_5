package com.example.demo;  // Deklarasi package

public class ForLoop {  // Deklarasi class "ForLoop"
    public static void main(String[] args) {  // Metode utama (main)
        for (;;) {  // Memulai perulangan tanpa henti (tidak memiliki kondisi berhenti)
            System.out.println("Perulangan tanpa henti");  // Cetak pesan perulangan tanpa henti
        }
    }
}
